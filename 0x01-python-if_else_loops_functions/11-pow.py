#!/usr/bin/python3

"""
Author: Wari Denyefa
Description: This script defines a function pow that takes
two parameters a and b and returns the result of raising
a to the power of b.
"""

def pow(a, b):
    """Calculates the power of a raised to the b-th power

    Args:
        a: base
        b: exponent

    Returns:
        result: a raised to the power of b
    """
    return a ** b
